public class PicgvItem {
    private int id;
    private int imgsrc;
    public PicgvItem(int id, int imgsrc) {
        this.id = id;
        this.imgsrc = imgsrc;
    }

    public int getId() {
        return id;
    }

    public int getImgsrc() {
        return imgsrc;
    }
}
